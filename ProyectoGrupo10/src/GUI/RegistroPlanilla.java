            colaborador.setSalarioXhoras((Integer) numSalarioXhora.getValue());
            colaborador.setHoras((Integer) numHoras.getValue());
            colaborador.setMes((Integer) numMes.getValue());
            colaborador.setAnio((Integer) numAnio.getValue());
