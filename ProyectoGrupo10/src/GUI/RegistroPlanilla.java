    public Float calcular(double horas, float salario) {
        total = (float) (horas * salario);
        return total;
    }

    public String creadorId() {
        Date date = new Date();
        String id = Sistema.Conexion.contador();
        id = id + "-" + date.getTime();
        return id;
    }

    public void ventanasMsjs(String msg) {

        switch (msg) {

            case "correcto":
                JOptionPane.showMessageDialog(null, "Accion ejecutada de forma correcta.", "Info", 1);
                break;

            case "duplicado":
                JOptionPane.showMessageDialog(null, "El dato ingresado esta duplicado.", "Info", 1);
                break;

            case "no existe":
                JOptionPane.showMessageDialog(null, "El dato ingresado no existe", "Info", 1);
                break;

            case "id vacio":
                JOptionPane.showMessageDialog(null, "El campo de id no puede estar vacio", "Campo vacio", 2);
                break;

            case "error base":
                JOptionPane.showMessageDialog(null, "Error al conectar la base de datos.", "Error", 1);
                break;
        }
    }

    private void presentarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jTable.getModel();
        jTable.setModel(modelo);

        TableModel modeloDatos = jTable.getModel();

        Vector<String> columnNames = new Vector<>();
        //int columnCount = (Integer.parseInt(contarAutos()));
        columnNames.add("ID");
        columnNames.add("Cedula");
        columnNames.add("Nombre");
        columnNames.add("Salario por Hora");
        columnNames.add("Mes");
        columnNames.add("Anio");
        columnNames.add("Horas trabajadas");
        columnNames.add("Total");

        modelo.setDataVector(listaPlanilla.vectorLista(), columnNames);

    }

